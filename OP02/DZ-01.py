# Задаем список с числами
numbers = [1, 2, 3, 4, 5]

# Инициализируем переменную для хранения суммы
total_sum = 0

# Используем цикл for для суммирования чисел в списке
for number in numbers:
    total_sum += number  # Добавляем текущее число к общей сумме

# Выводим результат
print("Сумма всех чисел в списке:", total_sum)