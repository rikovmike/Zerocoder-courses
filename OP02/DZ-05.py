# Создаем пустой список для хранения вводимых символов/цифр
user_inputs = []

# Цикл for для 10 итераций
for i in range(10):
    # Запрашиваем ввод от пользователя
    user_input = input(f"Введите символ или цифру {i + 1}: ")
    # Добавляем введенный символ/цифру в список
    user_inputs.append(user_input)

# Выводим итоговый список
print("Вы ввели следующие символы/цифры:", user_inputs)